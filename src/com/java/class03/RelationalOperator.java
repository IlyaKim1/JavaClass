package com.java.class03;

public class RelationalOperator {
    public static void main(String[] args) {
            int a = 10, b = 11;
            System.out.println(a > b); // f
            System.out.println(a < b); // t
            System.out.println(a >= b); // f
            System.out.println(a <= b); // t
            System.out.println(a != b); // t
            System.out.println(a == b); // f


              // ! - not
            int c = 10, d = 10;
            System.out.println(c > d); // f
            System.out.println(c < d); // f
            System.out.println(c >= d); // t
            System.out.println(c <= d); // t
            System.out.println(c != d); // f
            System.out.println(c == d); // t



    }
}
