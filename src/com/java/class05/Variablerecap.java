package com.java.class05;

public class Variablerecap {
    public static void main(String[] args) {

        int a; //Declaration of a variable.
        a = 10; // Assigning value to the variable.
        a = 20; // If you declare another value to the same variable the last value will be erased even if not deleted from the code.

        int b = 20; // Declaration and initialization.

        int c , d = 10, e; // Declaring multiple variable in the same line of same datatype.
    }
}
