package com.java.class14;

public class IncrementDecrementOperator {
    public static void main(String[] args) {
        int i = 1; //Initialization(Control of the loop)
//        i++, ++i Increment Operator
//
//        i--, --i Decrement Operator

        while(i <= 5){ //Condition(Control of the loop)
            System.out.println(i);
            i++; //Increment/Decrement(Control of the loop)
        }


    }
}
